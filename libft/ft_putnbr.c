#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
		return ;
	}
	else if ((n / 10) != 0)
		ft_putnbr(n / 10);
	ft_putchar(n - ((n / 10) * 10) + 48);
}
