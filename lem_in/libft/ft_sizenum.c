#include "libft.h"

int		ft_sizenum(int n)
{
	int	size;

	if (n > 0)
	{
		size = 0;
		n = -n;
	}
	else
		size = 1;
	while (n != 0)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
